/** @hideinitializer
@brief C++14 constexpr
@m_since{2020,06}

Expands to @cpp constexpr @ce if C++14 or newer standard is enabled and if the
compiler implements C++14 relaxed constexpr rules (which includes GCC 5+, Clang
3.5+ and MSVC 2017+), empty otherwise. Useful for selectively marking functions
that make use of C++14 constexpr.
@see @ref CORRADE_CXX_STANDARD, @ref CORRADE_CONSTEXPR20
*/
#if !defined(CORRADE_CONSTEXPR14) || defined(DOXYGEN_GENERATING_OUTPUT)
/* MSVC2015 reports itself as supporting C++14 in _MSVC_LANG (its variant of
   __cplusplus, see https://stackoverflow.com/a/74193034) but C++14 constexpr
   is only implemented since MSVC2017. The value of __cplusplus is set to
   201402 since GCC 5 and Clang 3.5 (https://stackoverflow.com/a/30997455),
   while C++14 constexpr is supported since GCC 5 and Clang 3.4. Since those
   are both quite old, the check can thus be coarser and just assume that C++14
   constexpr is supported only if __cplusplus is 201402. */
#if CORRADE_CXX_STANDARD >= 201402 && !defined(CORRADE_MSVC2015_COMPATIBILITY)
#define CORRADE_CONSTEXPR14 constexpr
#else
/* Needs to have non-empty contents to avoid acme.py removing the #define if
   #pragma ACME disable is used */
#define CORRADE_CONSTEXPR14 /*constexpr*/
#endif
#endif

/** @hideinitializer
@brief C++20 constexpr
@m_since_latest

Expands to @cpp constexpr @ce if C++20 or newer standard is enabled and if the
compiler implements *all* C++20 constexpr *language* additions such as trivial
default initialization (which includes GCC 10+, Clang 10+ and MSVC 2019
19.29+), empty otherwise. Useful for selectively marking functions that make
use of C++20 constexpr.
@see @ref CORRADE_CXX_STANDARD, @ref CORRADE_CONSTEXPR14
*/
#if !defined(CORRADE_CONSTEXPR20) || defined(DOXYGEN_GENERATING_OUTPUT)
/* Changing an active member of a union is __cpp_constexpr == 202002, but Clang
   16 reports only 201907 even though that feature is said to be available
   since Clang 9 already; same case is with GCC where it reports 202002 only
   since 12 while this is said to be supported since 10. So we use just 201907,
   hopefully that won't bite back. MSVC then reports 201603 in 19.28 but 201907
   in 19.29, so while 19.28 should have everything needed and 19.29 is only
   adding constinit, to not have to add a MSVC-specific special case we ignore
   19.28 and enable this on 19.29+ only for consistency.
    https://en.cppreference.com/w/cpp/compiler_support#cpp20
   This doesn't compare __cplusplus / CORRADE_CXX_STANDARD to 202002 because
   that's only set since Clang 11, GCC 11 and MSVC 19.30. */
#if __cpp_constexpr >= 201907
#define CORRADE_CONSTEXPR20 constexpr
#else
/* Needs to have non-empty contents to avoid acme.py removing the #define if
   #pragma ACME disable is used */
#define CORRADE_CONSTEXPR20 /*constexpr*/
#endif
#endif

/** @hideinitializer
@brief Always inline a function
@m_since{2019,10}

Stronger than the standard @cpp inline @ce keyword where supported, but even
then the compiler might decide to not inline the function (for example if it's
recursive). Expands to @cpp __attribute__((always_inline)) inline @ce on GCC
and Clang (both keywords need to be specified,
[docs](https://gcc.gnu.org/onlinedocs/gcc/extensions-to-the-c-language-family/declaring-attributes-of-functions/common-function-attributes.html#fn-attr-always_inline)),
to @cpp __forceinline @ce on MSVC ([docs](https://docs.microsoft.com/en-us/cpp/cpp/inline-functions-cpp))
and to just @cpp inline @ce elsewhere. On GCC and Clang this makes the function
inline also in Debug mode (`-g`), while on MSVC compiling in Debug (`/Ob0`)
always suppresses all inlining. Example usage:

@snippet Utility.cpp CORRADE_ALWAYS_INLINE

@see @ref CORRADE_NEVER_INLINE, @ref CORRADE_LIKELY(), @ref CORRADE_UNLIKELY()
*/
#ifndef CORRADE_ALWAYS_INLINE
#ifdef CORRADE_TARGET_GCC
#define CORRADE_ALWAYS_INLINE __attribute__((always_inline)) inline
#elif defined(CORRADE_TARGET_MSVC)
#define CORRADE_ALWAYS_INLINE __forceinline
#else
#define CORRADE_ALWAYS_INLINE inline
#endif
#endif

