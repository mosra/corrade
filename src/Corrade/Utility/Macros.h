/** @hideinitializer
@brief Assume a condition
@m_since{2020,06}

Compared to @ref CORRADE_INTERNAL_ASSERT() this macro does not handle the case
when the condition isn't @cpp true @ce in any way --- only provides a hint to
the compiler, possibly improving performance. Uses a compiler builtin on GCC,
Clang and MSVC; expands to an empty @cpp do while @ce otherwise. Example usage:

@snippet Utility.cpp CORRADE_ASSUME

You can override this implementation by placing your own
@cpp #define CORRADE_ASSUME @ce before including the
@ref Corrade/Utility/Macros.h header.

@see @ref CORRADE_ASSERT(), @ref CORRADE_ASSERT_UNREACHABLE(),
    @ref CORRADE_LIKELY(), @ref CORRADE_UNLIKELY()
*/
#ifndef CORRADE_ASSUME
#ifdef CORRADE_TARGET_CLANG
#define CORRADE_ASSUME(condition) __builtin_assume(condition)
#elif defined(CORRADE_TARGET_MSVC)
#define CORRADE_ASSUME(condition) __assume(condition)
#elif defined(CORRADE_TARGET_GCC)
#if __GNUC__ >= 13
#define CORRADE_ASSUME(condition) __attribute__((assume(condition)))
#else
#define CORRADE_ASSUME(condition)                                           \
    do {                                                                    \
        if(!(condition)) __builtin_unreachable(); \
    } while(false)
#endif
#else
#define CORRADE_ASSUME(condition) do {} while(false)
#endif
#endif

/** @hideinitializer
@brief Mark an if condition as likely to happen
@m_since_latest

Since branch predictors of contemporary CPUs do a good enough job already, the
main purpose of this macro is to affect assembly generation and instruction
cache use in hot loops --- for example, when a certain condition is likely to
happen each iteration, the compiler may put code of the @cpp else @ce branch in
a "cold" section of the code, ensuring the more probable code path stays in the
cache:

@snippet Utility.cpp CORRADE_LIKELY

Defined as the C++20 @cpp [[likely]] @ce attribute on GCC >= 10 (and on Clang
12 and MSVC 2019 16.6+ if compiling under C++20). On older versions of GCC and
on Clang in C++11/14/17 mode the macro expands to @cpp __builtin_expect() @ce;
on older MSVC the macro is a pass-through.

@attention
@parblock
Since the C++20 @cpp [[likely]] @ce attribute is expected to be put *after* the
@cpp if() @ce parentheses while @cpp __builtin_expect() @ce is meant to be
* *inside*, the macro has to be written as above. Doing
@cpp if(CORRADE_LIKELY(condition)) @ce would work on compilers that don't
understand the @cpp [[likely]] @ce attribute, but breaks in C++20 mode.
@endparblock

It's recommended to use this macro only if profiling shows its advantage. While
it may have no visible effect in most cases,
[wrong suggestion can lead to suboptimal performance](https://stackoverflow.com/a/35940041).
Similar effect can be potentially achieved by
[reordering the branches in a certain way](https://stackoverflow.com/q/46833310),
but the behavior is highly dependent on compiler-specific heuristics.
@see @ref CORRADE_UNLIKELY(), @ref CORRADE_ASSUME(),
    @ref CORRADE_ALWAYS_INLINE, @ref CORRADE_NEVER_INLINE
*/
#ifndef CORRADE_LIKELY
/* While the GCC 9 changelog mentions experimental support for [[likely]] and
   [[unlikely]], it warns that "attributes at the beginning of statement are
   ignored" (??). Version 10 treats them properly. For MSVC using > 201703
   because 16.6 reports 201705 when compiling as C++20.

   For the GCC branch explicitly checking we're not on Clang because certain
   Clang-based IDEs inherit __GNUC__ if GCC is used instead of leaving it at 4
   like Clang itself does, and thus this macro would cause a warning in the
   IDE if it didn't have Clang 12+. */
#if (defined(CORRADE_TARGET_GCC) && !defined(CORRADE_TARGET_CLANG) && __GNUC__ >= 10) || (CORRADE_CXX_STANDARD > 201703 && ((defined(CORRADE_TARGET_CLANG) && !defined(CORRADE_TARGET_APPLE_CLANG) && __clang_major__ >= 12) || (defined(CORRADE_TARGET_MSVC) && _MSC_VER >= 1926)))
#define CORRADE_LIKELY(...) (__VA_ARGS__) [[likely]]
#elif defined(CORRADE_TARGET_GCC)
#define CORRADE_LIKELY(...) (__builtin_expect((__VA_ARGS__), 1))
#else
#define CORRADE_LIKELY(...) (__VA_ARGS__)
#endif
#endif

